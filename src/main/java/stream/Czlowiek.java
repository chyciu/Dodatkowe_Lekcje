package stream;

public class Czlowiek {

    private int wiek;
    private String imie;

    public Czlowiek(int wiek, String imie) {
        this.wiek = wiek;
        this.imie = imie;
    }

    public int getWiek() {
        return wiek;
    }

    public String getImie() {
        return imie;
    }
}
